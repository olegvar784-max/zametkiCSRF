<?php
/**
 * Класс для защиты от CSRF-атак
 * 
 * CSRF (Cross-Site Request Forgery) — атака, при которой злоумышленник
 * заставляет браузер жертвы выполнить нежелательный запрос к сайту,
 * на котором жертва авторизована.
 * 
 * Принцип защиты:
 * 1. Сервер генерирует уникальный токен
 * 2. Токен сохраняется в сессии пользователя (не в БД — быстрее и безопаснее)
 * 3. Токен вставляется в каждую форму как скрытое поле
 * 4. При отправке формы сервер сравнивает токен из формы с токеном в сессии
 * 5. Если токены не совпадают — запрос отклоняется с кодом 403
 */
class CsrfGuard {
    
    /**
     * Генерация CSRF-токена
     * 
     * Использует random_bytes() — криптографически безопасный генератор.
     * Токен сохраняется в сессии, а не в БД, потому что:
     * - Токен привязан к конкретной сессии
     * - Нет лишних запросов к БД при каждой проверке
     * - Автоматически удаляется при завершении сессии
     * 
     * @return string Сгенерированный токен в hex-формате
     */
    public static function generate() {
        // 32 случайных байта = 256 бит энтропии (невозможно подобрать)
        $token = bin2hex(random_bytes(32));
        $_SESSION['csrf_token'] = $token;
        return $token;
    }
    
    /**
     * Проверка CSRF-токена из формы
     * 
     * Использует hash_equals() вместо обычного сравнения (===),
     * чтобы предотвратить timing-атаки. При обычном сравнении
     * злоумышленник может подбирать токен по времени ответа сервера.
     * hash_equals() сравнивает строки за константное время.
     * 
     * @param string $inputToken Токен, полученный из POST-запроса
     * @return bool true — токен верный, false — неверный или отсутствует
     */
    public static function validate($inputToken) {
        // Проверяем существование токена и в сессии, и во входных данных
        if (empty($_SESSION['csrf_token']) || empty($inputToken)) {
            error_log("CSRF: Отсутствует токен. IP: " . $_SERVER['REMOTE_ADDR']);
            return false;
        }
        
        // Безопасное сравнение за константное время
        if (hash_equals($_SESSION['csrf_token'], $inputToken)) {
            // Удаляем использованный токен (одноразовый)
            // Это предотвращает повторное использование токена
            unset($_SESSION['csrf_token']);
            return true;
        }
        
        // Логируем попытку с неверным токеном (возможная CSRF-атака)
        error_log("CSRF: Неверный токен. IP: " . $_SERVER['REMOTE_ADDR']);
        return false;
    }
    
    /**
     * Генерация HTML-кода для вставки в форму
     * 
     * Создаёт скрытое поле <input type="hidden"> с токеном.
     * Вызывается внутри каждой формы, которая изменяет данные (POST).
     * 
     * @return string HTML-код скрытого поля с токеном
     */
    public static function embed() {
        if (empty($_SESSION['csrf_token'])) {
            self::generate();
        }
        return '<input type="hidden" name="csrf_token" value="' 
               . htmlspecialchars($_SESSION['csrf_token']) . '">';
    }
    
    /**
     * Получить текущий токен (для ссылок GET)
     * 
     * Используется когда нужно передать токен через URL,
     * например в ссылке на удаление.
     * 
     * @return string Текущий CSRF-токен
     */
    public static function getToken() {
        if (empty($_SESSION['csrf_token'])) {
            self::generate();
        }
        return $_SESSION['csrf_token'];
    }
}